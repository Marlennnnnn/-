public class LightingSystem {
    public void dim(int level) {
        System.out.println("Освещение потускнело до " + level + "%.");
    }

    public void on() {
        System.out.println("Свет сейчас выключен.");
    }

    public void off() {
        System.out.println("Свет сейчас выключен.");
    }
}
